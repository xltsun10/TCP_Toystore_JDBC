package model;

/**
 *
 * @author lequa
 */
public class Order {
    private int id;
    private User user;
    private String orderDate;

    public Order() {
    }

    public Order(int id, User user, String orderDate) {
        this.id = id;
        this.user = user;
        this.orderDate = orderDate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public String getOrderDate() {
        return orderDate;
    }

    public void setOrderDate(String orderDate) {
        this.orderDate = orderDate;
    }
    
}
